pre = [0,0,0,0,0,0]
pre[0] = A[0]
pre[1] = A[0] + A[1]
pre[2] = A[0] + A[1] + A[2]
pre[3] = A[0] + A[1] + A[2] + A[3]

pre[0] = A[0]
pre[1] = pre[0] + A[1]
pre[2] = pre[1] + A[2]
pre[3] = pre[2] + A[3]

